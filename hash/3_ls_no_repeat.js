/**
 * @param {string} s
 * @return {number}
 */
var lengthOfLongestSubstring = function (s) {
    let map, maxLen, curLen, isRepeat;

    map = new Map();
    maxLen = curLen = 0;

    for (let i = 0; i < s.length; i++) {
        isRepeat = false;

        for (let j = i; j < s.length && !isRepeat; j++) {
            if (map.has(s[j])) {
                isRepeat = true;
            } else {
                map.set(s[j], true);
                curLen++;
            }
        }

        map = new Map();
        maxLen = Math.max(maxLen, curLen);
        curLen = 0;
    }

    return maxLen;
};

